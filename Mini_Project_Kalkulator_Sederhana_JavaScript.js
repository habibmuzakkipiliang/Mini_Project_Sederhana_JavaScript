let a = parseInt (prompt ("Masukkan angka A"))
let b = parseInt (prompt ("Masukkan angka B"))

// operator dasar

let hasil = a + b
console.log ("Hasilnya adalah (tambah)", hasil)

let km = a - b
console.log ("Hasilnya adalah (kurang)", km)

let rt = a * b
console.log ("Hasilnya adalah (kali)", rt)

let yu = a ** b
console.log ("Hasilnya adalah (pangkat)", yu)

let jk = a / b
console.log ("Hasilnya adalah (bagi)", jk)

let hj = a % b
console.log ("Hasilnya adalah (modulus)", hj)

// perbandingan

let ju = a > b
console.log ("Apakah A lebih besar dari B? :", ju)

let ik = a < b
console.log ("Apakah A lebih kecil dari B? :", ik)

let ol = a != b
console.log ("Apakah A tidak sama dengan B? :", ol)

// logika

let he = (a > b) && (a < b)
console.log ("Apakah A lebih besar dari B dan A lebih kecil dari B? :", he)

let kl = (a > b) || (a < b)
console.log ("Apakah A lebih besar dari B atau A lebih kecil dari B? :", kl)